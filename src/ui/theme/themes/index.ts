interface ThemeColors {
  bg: string
  bgSecondary: string
  bgTertiary: string
  surface: string
  surfaceSecondary: string
  text: string
  textSecondary: string
  textTertiary: string
  border: string
  borderSecondary: string
  accent: string
  accentHover: string
  accentText: string
  success: string
  warning: string
  error: string
  info: string
  diffAddBg: string
  diffAddText: string
  diffDelBg: string
  diffDelText: string
  syntaxKeyword: string
  syntaxString: string
  syntaxNumber: string
  syntaxFunction: string
  syntaxComment: string
  syntaxVariable: string
}

export interface ThemeDefinition {
  id: string
  name: string
  light: ThemeColors
  dark: ThemeColors
}

function defineTheme(id: string, name: string, light: ThemeColors, dark: ThemeColors): ThemeDefinition {
  return { id, name, light, dark }
}

